#!/usr/bin/env python3
"""Generate OA's bounded FP32 tiled-GEMM lattice from TOML.

The generator is stdlib-only and intentionally emits small include fragments:
CMake compile records, public fixed-kernel rows/IDs, and runtime matmul metadata.
The Slang arithmetic remains in one reviewed template.
"""
from __future__ import annotations

import argparse
import hashlib
import sys
import tomllib
from dataclasses import dataclass
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[2]
SCHEMA = REPO_ROOT / "Tools/OaTile/Schema/Fp32Tiled.toml"
BF16_SCHEMA = REPO_ROOT / "Tools/OaTile/Schema/Bf16Coop.toml"
DEFAULT_OUT = REPO_ROOT / "Tools/OaTile/Output"
MARKER = "// AUTO-GENERATED by Tools/OaTile/oatile.py -- DO NOT EDIT."


@dataclass(frozen=True)
class Epilogue:
	kind: str
	name: str
	local_id: int


@dataclass(frozen=True)
class Variant:
	name: str
	local_id: int
	tile_m: int
	tile_n: int
	tile_k: int
	thread_m: int
	thread_n: int
	workgroup: int
	group_m: int
	epilogues: tuple[Epilogue, ...]

	@property
	def shared_bytes(self) -> int:
		return (self.tile_m + self.tile_n) * (self.tile_k + 1) * 4


@dataclass(frozen=True)
class CompiledVariant:
	name: str
	local_id: int
	geometry: Variant
	epilogue: str


@dataclass(frozen=True)
class CoopVariant:
	name: str
	kind: str
	act: int


@dataclass(frozen=True)
class CoopFamily:
	template: str
	kernel: str
	tile_m: int
	tile_n: int
	tile_k: int
	workgroup: int
	caps: str
	variants: tuple[CoopVariant, ...]


EPILOGUES = {
	"none": ("None", False, False),
	"bias": ("Bias", True, False),
	"bias_relu": ("BiasRelu", True, True),
	"bias_gelu": ("BiasGelu", True, True),
}

COOP_EPILOGUES = {
	"none": ("None", False, False, False),
	"bias": ("Bias", True, False, False),
	"bias_relu": ("BiasRelu", True, True, False),
	"bias_gelu": ("BiasGelu", True, True, False),
	"silu_dual": ("SiluDual", False, True, True),
}


def load_coop_schema(path: Path) -> tuple[int, int, list[CoopFamily], int]:
	raw_bytes = path.read_bytes()
	data = tomllib.loads(raw_bytes.decode("utf-8"))
	version = int(data.get("schema_version", 0))
	abi = int(data.get("generator_abi", 0))
	if version != 1 or abi <= 0:
		fail("unsupported BF16 cooperative-matrix schema contract")
	families: list[CoopFamily] = []
	all_names: set[str] = set()
	for index, item in enumerate(data.get("family", [])):
		tile = item.get("tile", [])
		if len(tile) != 3 or min(map(int, tile)) <= 0:
			fail(f"family[{index}]: tile must contain three positive values")
		variants: list[CoopVariant] = []
		for raw in item.get("variants", []):
			kind, name, act = str(raw.get("kind", "")), str(raw.get("name", "")), int(raw.get("act", -1))
			if kind not in COOP_EPILOGUES or not name or act < 0:
				fail(f"family[{index}]: invalid BF16 variant")
			if name in all_names:
				fail(f"duplicate BF16 variant name {name}")
			all_names.add(name)
			variants.append(CoopVariant(name, kind, act))
		family = CoopFamily(
			template=str(item.get("template", "")), kernel=str(item.get("kernel", "")),
			tile_m=int(tile[0]), tile_n=int(tile[1]), tile_k=int(tile[2]),
			workgroup=int(item.get("workgroup", 0)), caps=str(item.get("caps", "")),
			variants=tuple(variants))
		if not family.template or not family.kernel or not family.caps or family.workgroup <= 0 or not variants:
			fail(f"family[{index}]: incomplete BF16 family")
		families.append(family)
	if not families:
		fail("BF16 schema contains no families")
	schema_hash = int.from_bytes(hashlib.sha256(raw_bytes).digest()[:8], "little")
	return version, abi, families, schema_hash


def compiled_variants(variants: list[Variant]) -> list[CompiledVariant]:
	result: list[CompiledVariant] = []
	for variant in variants:
		result.append(CompiledVariant(
			name=variant.name, local_id=variant.local_id,
			geometry=variant, epilogue="none"))
		result.extend(CompiledVariant(
			name=epilogue.name, local_id=epilogue.local_id,
			geometry=variant, epilogue=epilogue.kind)
			for epilogue in variant.epilogues)
	return result


def fail(message: str) -> None:
	raise ValueError(message)


def load_schema(path: Path) -> tuple[int, int, list[Variant], int]:
	raw_bytes = path.read_bytes()
	data = tomllib.loads(raw_bytes.decode("utf-8"))
	schema_version = int(data.get("schema_version", 0))
	generator_abi = int(data.get("generator_abi", 0))
	if schema_version != 1:
		fail(f"unsupported schema_version={schema_version}")
	if generator_abi <= 0:
		fail("generator_abi must be positive")

	variants: list[Variant] = []
	for index, item in enumerate(data.get("variant", [])):
		ctx = f"variant[{index}]"
		base_name = str(item.get("name", ""))
		if not base_name or not base_name.replace("_", "").isalnum() or not base_name[0].isalpha():
			fail(f"{ctx}: invalid C++/shader name {base_name!r}")
		tile = item.get("tile", [])
		thread_tile = item.get("thread_tile", [])
		if len(tile) != 3 or len(thread_tile) != 2:
			fail(f"{ctx}: tile must have 3 values and thread_tile 2")
		epilogues: list[Epilogue] = []
		for epilogue_index, epilogue_item in enumerate(item.get("epilogues", [])):
			ectx = f"{ctx}.epilogues[{epilogue_index}]"
			kind = str(epilogue_item.get("kind", ""))
			epilogue_name = str(epilogue_item.get("name", ""))
			local_id = int(epilogue_item.get("local_id", 0))
			if kind == "none" or kind not in EPILOGUES:
				fail(f"{ectx}: unsupported epilogue kind {kind!r}")
			if not epilogue_name or not epilogue_name.replace("_", "").isalnum() or not epilogue_name[0].isalpha():
				fail(f"{ectx}: invalid C++/shader name {epilogue_name!r}")
			if local_id <= 0:
				fail(f"{ectx}: local_id must be positive")
			epilogues.append(Epilogue(kind=kind, name=epilogue_name, local_id=local_id))
		v = Variant(
			name=base_name,
			local_id=int(item.get("local_id", 0)),
			tile_m=int(tile[0]), tile_n=int(tile[1]), tile_k=int(tile[2]),
			thread_m=int(thread_tile[0]), thread_n=int(thread_tile[1]),
			workgroup=int(item.get("workgroup", 0)),
			group_m=int(item.get("group_m", 0)),
			epilogues=tuple(epilogues),
		)
		if min(v.local_id, v.tile_m, v.tile_n, v.tile_k,
		       v.thread_m, v.thread_n, v.workgroup) <= 0 or v.group_m < 0:
			fail(f"{ctx}: dimensions/IDs must be positive and group_m non-negative")
		if v.tile_m % v.thread_m or v.tile_n % v.thread_n:
			fail(f"{ctx}: tile must divide evenly into thread_tile")
		threads = (v.tile_m // v.thread_m) * (v.tile_n // v.thread_n)
		if threads != v.workgroup:
			fail(f"{ctx}: thread grid requires {threads}, schema says {v.workgroup}")
		for label, elements in (("A", v.tile_m * v.tile_k), ("B", v.tile_n * v.tile_k)):
			if elements % (v.workgroup * 4):
				fail(f"{ctx}: {label} tile cannot be distributed as float4 loads")
		if v.tile_k % 4:
			fail(f"{ctx}: tile_k must be a multiple of four")
		variants.append(v)

	if not variants:
		fail("schema contains no variants")
	compiled = compiled_variants(variants)
	if len({v.name for v in compiled}) != len(compiled):
		fail("duplicate variant name")
	if len({v.local_id for v in compiled}) != len(compiled):
		fail("duplicate local_id")
	schema_hash = int.from_bytes(hashlib.sha256(raw_bytes).digest()[:8], "little")
	return schema_version, generator_abi, variants, schema_hash


def generated_header(schema_rel: str) -> list[str]:
	return [MARKER, f"// Source of truth: {schema_rel}", ""]


def emit_cmake(variants: list[Variant], schema_rel: str) -> str:
	lines = [
		"# AUTO-GENERATED by Tools/OaTile/oatile.py -- DO NOT EDIT.",
		f"# Source of truth: {schema_rel}",
		"",
	]
	compiled = compiled_variants(variants)
	fields = {
		"NAMES": [v.name for v in compiled],
		"IDS": [str(v.local_id) for v in compiled],
		"TILE_M": [str(v.geometry.tile_m) for v in compiled],
		"TILE_N": [str(v.geometry.tile_n) for v in compiled],
		"TILE_K": [str(v.geometry.tile_k) for v in compiled],
		"THREAD_M": [str(v.geometry.thread_m) for v in compiled],
		"THREAD_N": [str(v.geometry.thread_n) for v in compiled],
		"WORKGROUP": [str(v.geometry.workgroup) for v in compiled],
		"GROUP_M": [str(v.geometry.group_m) for v in compiled],
		"EPILOGUE": [str(list(EPILOGUES).index(v.epilogue)) for v in compiled],
	}
	for suffix, values in fields.items():
		lines.append(f"set(OA_TILE_FP32_{suffix}")
		lines.extend(f"\t{value}" for value in values)
		lines.append(")")
	return "\n".join(lines) + "\n"


def emit_kernel_rows(variants: list[Variant], schema_rel: str) -> str:
	lines = generated_header(schema_rel)
	for v in compiled_variants(variants):
		lines.append(
			f'\t{{ "{v.name}", OA_COMPUTE_KERNEL_ID(OaComputeKernelPrefix::Ml, '
			f'{v.local_id}), OaComputeKernelCategory::Ml, "oa" }},')
	return "\n".join(lines) + "\n"


def emit_compute_ids(variants: list[Variant], schema_rel: str) -> str:
	lines = generated_header(schema_rel)
	for v in compiled_variants(variants):
		lines.append(
			f"static constexpr OaKernelId {v.name} = "
			f"OA_COMPUTE_KERNEL_ID(OaComputeKernelPrefix::Ml, {v.local_id});")
	return "\n".join(lines) + "\n"


def emit_matmul_rows(variants: list[Variant], schema_rel: str) -> str:
	lines = generated_header(schema_rel)
	for v in compiled_variants(variants):
		ep_name, supports_bias, supports_activation = EPILOGUES[v.epilogue]
		geometry = v.geometry
		lines.extend([
			f'\t{{OaMatmulVariantIdFromName("{v.name}"), "{v.name}",',
			f"\t OaGemmKernel::TiledFp32, OaGemmPath::Standard, OaGemmEpilogue::{ep_name},",
			"\t OaStoragePrecision::Fp32, OaStoragePrecision::Fp32,",
			"\t OaStoragePrecision::Fp32, OaStoragePrecision::Fp32,",
			f"\t {geometry.tile_m}, {geometry.tile_n}, {geometry.tile_k}, {geometry.workgroup},",
			f"\t false, true, {str(supports_bias).lower()}, "
			f"{str(supports_activation).lower()}, false, {geometry.shared_bytes},",
			"\t kCapTiledFp32},",
			"",
		])
	return "\n".join(lines) + "\n"


def emit_metadata(schema_version: int, generator_abi: int, schema_hash: int,
	              schema_rel: str) -> str:
	lines = generated_header(schema_rel)
	lines.extend([
		f"static constexpr OaU32 OaTileFp32SchemaVersion = {schema_version}U;",
		f"static constexpr OaU32 OaTileGeneratorAbi = {generator_abi}U;",
		f"static constexpr OaU64 OaTileFp32SchemaHash = 0x{schema_hash:016x}ULL;",
	])
	return "\n".join(lines) + "\n"


def emit_coop_cmake(families: list[CoopFamily], schema_rel: str) -> str:
	lines = ["# AUTO-GENERATED by Tools/OaTile/oatile.py -- DO NOT EDIT.",
		f"# Source of truth: {schema_rel}", ""]
	for family in families:
		var = "OA_GEMM_BF16_VARIANTS" if family.template == "GemmCmSgBf16" else "OA_GEMM_WG_BF16_VARIANTS"
		lines.append(f"set({var}")
		for variant in family.variants:
			lines.append(f'\t"{variant.name}" "{variant.act}"')
		lines.append(")")
	return "\n".join(lines) + "\n"


def emit_coop_rows(families: list[CoopFamily], schema_rel: str) -> str:
	lines = generated_header(schema_rel)
	for family in families:
		for variant in family.variants:
			ep, bias, activation, dual = COOP_EPILOGUES[variant.kind]
			lines.extend([
				f'\t{{OaMatmulVariantIdFromName("{variant.name}"), "{variant.name}",',
				f"\t OaGemmKernel::{family.kernel}, OaGemmPath::Standard, OaGemmEpilogue::{ep},",
				"\t OaStoragePrecision::Bf16, OaStoragePrecision::Bf16,",
				"\t OaStoragePrecision::Fp32, OaStoragePrecision::Fp32,",
				f"\t {family.tile_m}, {family.tile_n}, {family.tile_k}, {family.workgroup},",
				f"\t false, true, {str(bias).lower()}, {str(activation).lower()}, {str(dual).lower()}, 0,",
				f"\t {family.caps}}},", ""])
	return "\n".join(lines) + "\n"


def emit_coop_metadata(version: int, abi: int, schema_hash: int, schema_rel: str) -> str:
	lines = generated_header(schema_rel)
	lines.extend([
		f"static constexpr OaU32 OaTileBf16SchemaVersion = {version}U;",
		f"static constexpr OaU32 OaTileBf16GeneratorAbi = {abi}U;",
		f"static constexpr OaU64 OaTileBf16SchemaHash = 0x{schema_hash:016x}ULL;",
	])
	return "\n".join(lines) + "\n"


def write(path: Path, content: str, dry_run: bool) -> None:
	if dry_run:
		print(path)
		return
	path.parent.mkdir(parents=True, exist_ok=True)
	path.write_text(content, encoding="utf-8")
	print(f"wrote {path.relative_to(REPO_ROOT) if path.is_relative_to(REPO_ROOT) else path}")


def main() -> int:
	parser = argparse.ArgumentParser(description="Generate OaTile FP32 GEMM variants")
	parser.add_argument("--schema", type=Path, default=SCHEMA)
	parser.add_argument("--bf16-schema", type=Path, default=BF16_SCHEMA)
	parser.add_argument("--out", type=Path, default=DEFAULT_OUT)
	parser.add_argument("--live", action="store_true", help="write generated files into the repo Source tree")
	parser.add_argument("--dry-run", action="store_true")
	args = parser.parse_args()
	out = REPO_ROOT if args.live else args.out.resolve()
	schema = args.schema.resolve()
	schema_version, generator_abi, variants, schema_hash = load_schema(schema)
	schema_rel = schema.relative_to(REPO_ROOT).as_posix()
	bf16_schema = args.bf16_schema.resolve()
	bf16_version, bf16_abi, bf16_families, bf16_hash = load_coop_schema(bf16_schema)
	bf16_rel = bf16_schema.relative_to(REPO_ROOT).as_posix()

	outputs = {
		Path("Source/Private/Oa/Core/Shader/Compute/Blas/Tile/OaTileFp32Variants.gen.cmake"):
			emit_cmake(variants, schema_rel),
		Path("Source/Public/Oa/Core/OaTileKernelRegistry.gen.inc"):
			emit_kernel_rows(variants, schema_rel),
		Path("Source/Public/Oa/Runtime/OaTileComputeKernel.gen.h"):
			emit_compute_ids(variants, schema_rel),
		Path("Source/Private/Oa/Runtime/Gemm/OaTileFp32Variants.gen.inc"):
			emit_matmul_rows(variants, schema_rel),
		Path("Source/Private/Oa/Runtime/Gemm/OaTileMetadata.gen.h"):
			emit_metadata(schema_version, generator_abi, schema_hash, schema_rel),
		Path("Source/Private/Oa/Core/Shader/Compute/Blas/Tile/OaTileBf16Variants.gen.cmake"):
			emit_coop_cmake(bf16_families, bf16_rel),
		Path("Source/Private/Oa/Runtime/Gemm/OaTileBf16Variants.gen.inc"):
			emit_coop_rows(bf16_families, bf16_rel),
		Path("Source/Private/Oa/Runtime/Gemm/OaTileBf16Metadata.gen.h"):
			emit_coop_metadata(bf16_version, bf16_abi, bf16_hash, bf16_rel),
	}
	for rel, content in outputs.items():
		write(out / rel, content, args.dry_run)
	return 0


if __name__ == "__main__":
	try:
		sys.exit(main())
	except (OSError, ValueError, tomllib.TOMLDecodeError) as exc:
		print(f"oatile: {exc}", file=sys.stderr)
		sys.exit(1)
