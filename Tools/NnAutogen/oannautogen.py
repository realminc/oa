#!/usr/bin/env python3
"""
OaNnAutogen - schema-driven generator for OaModule layer wrappers.

This is intentionally small: NN layers are public API classes, so the first
slice only generates simple OaFnMatrix-backed modules where the boilerplate is
mechanical and the runtime policy stays inside OaContext/OaFn*.
"""
from __future__ import annotations

import argparse
import re
import sys
import tomllib
from pathlib import Path

TOOLS_ROOT = Path(__file__).resolve().parents[1]
if str(TOOLS_ROOT) not in sys.path:
	sys.path.insert(0, str(TOOLS_ROOT))

from autogen_io import write_generated_text


REPO_ROOT = Path(__file__).resolve().parents[2]
SCHEMA_DIR = REPO_ROOT / "Tools" / "NnAutogen" / "Schema"
DEFAULT_OUTPUT = REPO_ROOT / "Tools" / "NnAutogen" / "Output"
LIVE_SOURCE_ROOT = REPO_ROOT / "Source"


GEN_HEADER = """// AUTO-GENERATED by Tools/NnAutogen/oannautogen.py - DO NOT EDIT.
// Source of truth: Tools/NnAutogen/Schema/{schema_name}.
// Regenerate via: python3 Tools/NnAutogen/oannautogen.py --live
"""


def fail(message: str) -> None:
	print(f"oannautogen: ERROR: {message}", file=sys.stderr)
	raise SystemExit(1)


def schema_label(schema_path: Path) -> str:
	try:
		return schema_path.relative_to(SCHEMA_DIR).as_posix()
	except ValueError:
		return schema_path.name


def validate_layer(schema_path: Path, layer: dict) -> None:
	name = layer.get("name")
	if not isinstance(name, str) or not name:
		fail(f"{schema_path}: layer missing non-empty name")
	if not name[0].isupper() or any(ch == "_" for ch in name):
		fail(f"{schema_path}:{name}: layer name must be PascalCase")
	if name != "Silu" and any(part in name for part in ("ReLU", "GELU", "SiLU")):
		fail(f"{schema_path}:{name}: use PascalCase names such as Relu/Gelu/Silu")
	fn = layer.get("fn")
	forward_body = layer.get("forward_body")
	if (not isinstance(fn, str) or not fn) and (not isinstance(forward_body, str) or not forward_body.strip()):
		fail(f"{schema_path}:{name}: layer missing fn expression or forward_body")
	if "fn" in layer and (not isinstance(fn, str) or not fn):
		fail(f"{schema_path}:{name}: fn must be a non-empty string")
	if "forward_body" in layer and (not isinstance(forward_body, str) or not forward_body.strip()):
		fail(f"{schema_path}:{name}: forward_body must be a non-empty string")
	if "constructor_args" in layer and not isinstance(layer["constructor_args"], str):
		fail(f"{schema_path}:{name}: constructor_args must be a string")
	if "constructor_body" in layer and not isinstance(layer["constructor_body"], list):
		fail(f"{schema_path}:{name}: constructor_body must be a list of strings")
	if "extra_methods" in layer and not isinstance(layer["extra_methods"], str):
		fail(f"{schema_path}:{name}: extra_methods must be a string containing raw C++ code")
	if "extra_includes" in layer and not isinstance(layer["extra_includes"], str):
		fail(f"{schema_path}:{name}: extra_includes must be a string containing #include directives")
	path = layer.get("path", name)
	if not isinstance(path, str) or not path or Path(path).is_absolute() or ".." in Path(path).parts:
		fail(f"{schema_path}:{name}: path must be a safe relative output directory")
	if Path(path).name != name:
		fail(f"{schema_path}:{name}: path must end in the layer name")


def out_paths(layer: dict, out_root: Path, *, live: bool) -> tuple[Path, Path]:
	layer_name = layer["name"]
	base = out_root / "Private" / "Oa" / "Ml" / "Nn" / layer.get("path", layer_name)
	return base / f"{layer_name}.gen.h", base / f"{layer_name}.gen.cpp"


def cpp_param_list_without_defaults(params: str) -> str:
	return re.sub(r"\s*=\s*[^,]+", "", params)


def emit_header(layer: dict, schema_name: str) -> str:
	name = layer["name"]
	class_name = f"Oa{name}"
	notes = layer.get("notes", "")
	constructor = layer.get("constructor")
	constructor_args = layer.get("constructor_args")
	constructor_explicit = layer.get("constructor_explicit", False)
	methods = layer.get("methods", [])
	members = layer.get("members", [])
	lines = [GEN_HEADER.format(schema_name=schema_name)]
	lines.append("#pragma once\n")
	lines.append(f"/// {class_name}: {notes}")
	lines.append(f"class {class_name} : public OaModule {{")
	lines.append("public:")
	# Emit both default constructor (if specified) and parameterized constructor
	if constructor:
		lines.append(f"\t{constructor}")
	if constructor_args is not None:
		explicit_prefix = "explicit " if constructor_explicit else ""
		lines.append(f"\t{explicit_prefix}{class_name}({constructor_args});")
	elif not constructor:
		lines.append(f"\t{class_name}() = default;")
	lines.append("\tOaMatrix Forward(const OaMatrix& InInput) override;")
	for method in methods:
		lines.append(f"\t{method}")
	if members:
		lines.append("")
		lines.append("private:")
		for member in members:
			lines.append(f"\t{member}")
	lines.append("};")
	lines.append("")
	return "\n".join(lines)


def emit_cpp(layer: dict, schema_name: str) -> str:
	name = layer["name"]
	class_name = f"Oa{name}"
	fn = layer.get("fn")
	forward_body = layer.get("forward_body")
	notes = layer.get("notes", "")
	constructor_args = layer.get("constructor_args")
	constructor_init = layer.get("constructor_init", "")
	constructor_body = layer.get("constructor_body", [])
	extra_methods = layer.get("extra_methods", "")
	extra_includes = layer.get("extra_includes", "")
	constructor_def = ""
	if constructor_args is not None:
		cpp_constructor_args = cpp_param_list_without_defaults(constructor_args)
		init = f"\n\t: {constructor_init}" if constructor_init else ""
		body_lines = "\n".join(f"\t{line}" for line in constructor_body)
		constructor_def = (
			f"{class_name}::{class_name}({cpp_constructor_args})"
			f"{init} {{\n"
			f"{body_lines}\n"
			"}\n\n"
		)
	if forward_body is None:
		forward_body = f"\treturn {fn};\n"
	elif not forward_body.endswith("\n"):
		forward_body += "\n"
	body_uses_autograd = ("OaAutograd" in forward_body or "OaMakeSharedPtr" in forward_body)
	autograd_include = "#include <Oa/Ml/Autograd/Nodes.h>\n" if body_uses_autograd else ""
	
	return (
		GEN_HEADER.format(schema_name=schema_name)
		+ f"// {class_name} - {notes}\n\n"
		+ "#include <Oa/Ml/Nn.h>\n"
		+ "#include <Oa/Core/FnMatrix.h>\n"
		+ autograd_include
		+ "#include <Oa/Core/Validation.h>\n"
		+ extra_includes
		+ "\n"
		+ constructor_def
		+ extra_methods
		+ f"OaMatrix {class_name}::Forward(const OaMatrix& InInput) {{\n"
		+ forward_body
		+ "}\n"
	)


def generated_base(out_root: Path) -> Path:
	return out_root / "Private" / "Oa" / "Ml" / "Nn"


def emit_umbrella(layers: list[tuple[str, str]]) -> str:
	lines = [GEN_HEADER.format(schema_name="all NN schemas")]
	lines.append("#pragma once")
	lines.append("")
	for name, path in layers:
		lines.append(f'#include "{path}/{name}.gen.h"')
	lines.append("")
	return "\n".join(lines)


def emit_cmake_sources(layers: list[tuple[str, str]]) -> str:
	lines = [
		"# AUTO-GENERATED by Tools/NnAutogen/oannautogen.py - DO NOT EDIT.",
		"# Regenerate via: python3 Tools/NnAutogen/oannautogen.py --live",
		"",
		"set(OA_NN_GENERATED_SOURCES",
	]
	for name, path in layers:
		lines.append(f"\tSource/Private/Oa/Ml/Nn/{path}/{name}.gen.cpp")
	lines.append(")")
	lines.append("")
	return "\n".join(lines)


def write_manifest_files(layers: list[tuple[str, str]], out_root: Path, *, dry_run: bool) -> None:
	base = generated_base(out_root)
	umbrella_path = base / "Nn.gen.h"
	cmake_path = base / "NnSources.gen.cmake"
	if dry_run:
		print(f"  would write: {umbrella_path}")
		print(f"  would write: {cmake_path}")
		return
	write_generated_text(umbrella_path, emit_umbrella(layers))
	write_generated_text(cmake_path, emit_cmake_sources(layers))
	print(f"  wrote {umbrella_path.name}, {cmake_path.name}")


def process_schema(schema_path: Path, out_root: Path, *, live: bool, dry_run: bool) -> list[tuple[str, str]]:
	with schema_path.open("rb") as f:
		data = tomllib.load(f)
	layers = data.get("layers", [])
	if not layers:
		fail(f"{schema_path}: no [[layers]] entries")
	for layer in layers:
		validate_layer(schema_path, layer)
	schema_name = schema_label(schema_path)
	print(f"oannautogen: {schema_path.name} - {len(layers)} layers")
	for layer in layers:
		header_path, cpp_path = out_paths(layer, out_root, live=live)
		if dry_run:
			print(f"  would write: {header_path}")
			print(f"  would write: {cpp_path}")
			continue
		write_generated_text(header_path, emit_header(layer, schema_name))
		write_generated_text(cpp_path, emit_cpp(layer, schema_name))
		print(f"  wrote {header_path.name}, {cpp_path.name}")
	return [(layer["name"], layer.get("path", layer["name"])) for layer in layers]


def main() -> int:
	parser = argparse.ArgumentParser(description="Generate OaModule NN layer wrappers from schema.")
	parser.add_argument("--schema-dir", type=Path, default=SCHEMA_DIR)
	parser.add_argument("--schema", type=Path, default=None)
	parser.add_argument("--out", type=Path, default=DEFAULT_OUTPUT)
	parser.add_argument("--live", action="store_true")
	parser.add_argument("--dry-run", action="store_true")
	args = parser.parse_args()
	if args.live and args.schema and not args.dry_run:
		fail("--schema cannot be combined with --live because Nn.gen.h and NnSources.gen.cmake require every schema")

	out_root = LIVE_SOURCE_ROOT if args.live else args.out
	if args.live:
		print("oannautogen: LIVE MODE - writing to Source/ tree")
	schemas = [args.schema] if args.schema else sorted(args.schema_dir.rglob("*.toml"))
	if not schemas:
		fail(f"no schemas found in {args.schema_dir}")
	layers: list[tuple[str, str]] = []
	for schema_path in schemas:
		layers.extend(process_schema(schema_path, out_root, live=args.live, dry_run=args.dry_run))
	print(f"oannautogen: generated NN manifest - {len(layers)} layers")
	write_manifest_files(layers, out_root, dry_run=args.dry_run)
	return 0


if __name__ == "__main__":
	raise SystemExit(main())
