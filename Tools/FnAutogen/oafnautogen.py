def _emit_autograd_backward_body(op: dict) -> str | None:
	"""Generate the Backward() body for an op based on its formula field.
	Returns None for 'manual' or unsupported formulas."""
	ag = op.get("autograd", {})
	formula = ag.get("formula", "manual")
	if formula == "manual":
		return None

	# Determine scalar member name from ctor_args
	ctor_args = ag.get("ctor_args", [])
	scalar_member = ""
	if ctor_args:
		# InScalar -> Scalar_, InExponent -> Exponent_, etc.
		arg = ctor_args[0]
		if arg.startswith("In"):
			scalar_member = arg[2:] + "_"
		else:
			scalar_member = arg + "_"

	B = "\t\t"  # two tabs: inside class, inside Backward()
	body = ""
	if formula == "auto_binary_add":
		body = (
			f"{B}if (OutDIn.Size() > 0) OutDIn[0] = InDOut;\n"
			f"{B}if (OutDIn.Size() > 1) OutDIn[1] = InDOut;\n"
		)
	elif formula == "auto_binary_sub":
		body = (
			f"{B}if (OutDIn.Size() > 0) OutDIn[0] = InDOut;\n"
			f"{B}if (OutDIn.Size() > 1) OutDIn[1] = OaFnMatrix::Scale(InDOut, -1.0f);\n"
		)
	elif formula == "auto_binary_mul":
		body = (
			f"{B}const OaMatrix& a = Saved_[0];\n"
			f"{B}const OaMatrix& b = Saved_[1];\n"
			f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Mul(InDOut, b);\n"
			f"{B}if (OutDIn.Size() > 1) OutDIn[1] = OaFnMatrix::Mul(InDOut, a);\n"
		)
	elif formula == "auto_unary_identity":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = InDOut;\n"
	elif formula == "auto_unary_neg":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Scale(InDOut, -1.0f);\n"
	elif formula == "auto_unary_log":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Div(InDOut, Saved_[0]);\n"
	elif formula == "auto_unary_sqrt":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Div(InDOut, OaFnMatrix::Scale(Saved_[0], 2.0f));\n"
	elif formula == "auto_unary_exp":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Mul(InDOut, Saved_[0]);\n"
	elif formula == "auto_unary_sin":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Mul(InDOut, OaFnMatrix::Cos(Saved_[0]));\n"
	elif formula == "auto_unary_cos":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Scale(OaFnMatrix::Mul(InDOut, OaFnMatrix::Sin(Saved_[0])), -1.0f);\n"
	elif formula == "auto_unary_reciprocal":
		body = (
			f"{B}if (OutDIn.Size() > 0) {{\n"
			f"{B}\tconst OaMatrix& out = Saved_[0];\n"
			f"{B}\tOutDIn[0] = OaFnMatrix::Scale(OaFnMatrix::Mul(InDOut, OaFnMatrix::Mul(out, out)), -1.0f);\n"
			f"{B}}}\n"
		)
	elif formula == "auto_unary_scalar_identity":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = InDOut;\n"
	elif formula == "auto_unary_scalar_scale":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Scale(InDOut, {scalar_member});\n"
	elif formula == "auto_unary_scalar_div_scalar":
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::Scale(InDOut, 1.0f / {scalar_member});\n"
	elif formula == "auto_unary_scalar_pow":
		body = (
			f"{B}if (OutDIn.Size() > 0) {{\n"
			f"{B}\tconst OaMatrix& x = Saved_[0];\n"
			f"{B}\tauto dPow = OaFnMatrix::Pow(x, {scalar_member} - 1.0f);\n"
			f"{B}\tOutDIn[0] = OaFnMatrix::Scale(OaFnMatrix::Mul(InDOut, dPow), {scalar_member});\n"
			f"{B}}}\n"
		)
	elif formula == "auto_bwd_kernel":
		bwd_name = op["name"] + "Bwd"
		# Thread the saved scalar hyperparam (e.g. LeakyRelu/Elu alpha) into the
		# backward kernel. Without this the *Bwd call falls back to its defaulted
		# alpha, so a non-default alpha silently produces wrong input gradients.
		# scalar_member is non-empty only when the autograd block declares ctor_args.
		extra = f", {scalar_member}" if scalar_member else ""
		body = f"{B}if (OutDIn.Size() > 0) OutDIn[0] = OaFnMatrix::{bwd_name}(Saved_[0], InDOut{extra});\n"
	else:
		return None
	return body


def emit_autograd_class(op: dict) -> str:
	"""Emit `class OaGrad<Name> : public OaGradNode { ... };` for one op.
	Generates a full Backward() body when formula is auto-*. Manual gradient
	classes live in hand-written Autograd*.h files and deliberately emit
	nothing here; a generated file containing only "hand-written elsewhere"
	comments is not an API surface and must not exist."""
	ag = op.get("autograd", {})
	if not ag or not ag.get("grad_class"):
		return ""
	name = op["name"]
	grad_class = ag["grad_class"]

	# Scalar member lines
	scalar_decls = ""
	scalar_ctor = ""
	if ag.get("ctor_args"):
		# Emit ctor with typed args + member initialization
		# Derive member name from ctor arg (InScalar -> Scalar_)
		ctor_args = ag["ctor_args"]
		params = []
		inits = []
		members = []
		for arg in ctor_args:
			# Heuristic: arg like "InScalar" -> member "Scalar_"
			if arg.startswith("In"):
				member = arg[2:] + "_"
			else:
				member = arg + "_"
			params.append(f"OaF32 {arg}")
			inits.append(f"{member}({arg})")
			members.append(f"\tOaF32 {member};")
		scalar_ctor = (
			f"\texplicit {grad_class}({', '.join(params)}) noexcept "
			f": {', '.join(inits)} {{}}\n"
		)
		scalar_decls = "\n".join(members) + "\n"

	body = _emit_autograd_backward_body(op)
	if body is None:
		return ""

	# Auto-generated full class with Backward body
	return (
		f"class {grad_class} final : public OaGradNode {{\n"
		f"public:\n"
		f"{scalar_ctor}"
		f"\tvoid Backward(const OaMatrix& InDOut, OaVec<OaMatrix>& OutDIn) override {{\n"
		f"{body}"
		f"\t}}\n"
		f"{scalar_decls}"
		f"}};\n"
	)


def emit_autograd_header(ops: list[dict], schema_name: str, category: str, autograd_subdir: str = "Matrix") -> str:
	body_lines = []
	for op in ops:
		c = emit_autograd_class(op)
		if c:
			body_lines.append(c)
	if not body_lines:
		return ""
	lines = "\n".join(body_lines)
	return (
		f"// AUTO-GENERATED by Tools/FnAutogen/oafnautogen.py — DO NOT EDIT.\n"
		f"// Source of truth: Tools/FnAutogen/Schema/{schema_name}.\n"
		f"// Output: Source/Private/Oa/Ml/Autograd/{autograd_subdir}/Autograd{category}.gen.h\n"
		f"// Regenerate via: python3 Tools/FnAutogen/oafnautogen.py --live\n"
		f"\n"
		f"#pragma once\n"
		f"\n"
		f"#include <Oa/Ml/Autograd.h>\n"
		f"#include <Oa/Core/FnMatrix.h>\n"
		f"#include <Oa/Ml/FnMatrix.h>\n"
		f"\n"
		f"// ─── {category} gradient nodes (autogenerated) ─────────────────────────────\n"
		f"\n"
		f"{lines}\n"
	)


