def _dispatch_id_arg(op: dict) -> str:
	"""Argument for OaVkDispatch::Run (string literal or OaComputeKernelId::*)."""
	if "dispatch_id_macro" in op:
		return f"OaComputeKernelId::{op['dispatch_id_macro']}"
	return f'"{op["kernel_forward"]}"'


def _output_alloc_stmt(op: dict, namespace: str = "OaFnMatrix") -> str:
	"""C++ statement: `OaMatrix out = ...;` matching the op's output spec."""
	out_spec = op.get("output", {})
	shape = out_spec.get("shape", "match_input")
	dtype = out_spec.get("dtype", "match_input")
	kind = op["kind"]
	
	# For nullary ops, we need explicit shape/dtype from extra_params or output spec
	if kind == "nullary_scalar":
		dt_expr = f"OaScalarType::{dtype}" if dtype != "match_input" else "OaScalarType::Float32"
		# nullary ops must specify shape via extra_params or output.shape
		if shape == "scalar_1d":
			return f"OaMatrix out = OaFnMatrix::Zeros(OaMatrixShape{{1}}, {dt_expr});"
		# For Fill, shape comes from extra_params (InShape)
		return f"OaMatrix out = OaFnMatrix::Empty(InShape, {dt_expr});"
	
	dt_expr = "InA.Dtype_" if dtype == "match_input" else f"OaScalarType::{dtype}"
	# Always use OaFnMatrix for utility functions (Zeros, Empty)
	if shape == "scalar_1d":
		return f"OaMatrix out = OaFnMatrix::Zeros(OaMatrixShape{{1}}, {dt_expr});"
	if shape == "last_dim_half":
		scalar_name = op.get("scalar_param", {}).get("name")
		param_check = (
			f" and static_cast<OaI64>(In{scalar_name}) == InA.Size(-1) / 2"
			if scalar_name else ""
		)
		return (
			"OaMatrixShape outShape = InA.Shape_;\n"
			f"\tassert(outShape.Rank > 0 and InA.Size(-1) % 2 == 0{param_check});\n"
			"\toutShape.Dims[static_cast<OaUsize>(outShape.Rank - 1)] = InA.Size(-1) / 2;\n"
			f"\tOaMatrix out = OaFnMatrix::Empty(outShape, {dt_expr});"
		)
	# default: match input
	return f"OaMatrix out = OaFnMatrix::Empty(InA.Shape_, {dt_expr});"


def _dispatch_workgroups_expr(op: dict) -> str:
	wg = op.get("dispatch_workgroups", "elemwise")
	if isinstance(wg, int):
		return str(wg)
	if wg == "elemwise":
		return "DivCeil(n, 256)"
	if wg == "output_elemwise":
		return "DivCeil(static_cast<OaU32>(out.NumElements()), 256)"
	fail(f"{op['name']}: unsupported dispatch_workgroups {wg!r}")
	return "1"  # unreachable


def emit_autograd_attach(
	op: dict, semantic_operation: str | None = None
) -> str:
	"""Emits the forward-attach tail for an op that declares a [ops.autograd] block.

	The block is purely the *attach* — it constructs a hand-written OaGradXxx
	(defined in <Oa/Ml/Autograd.h>) and assigns it to out.MutAutograd().GradFn
	when (a) the tape is active and (b) any of the declared inputs requires grad.

	Schema (under [ops.autograd]):
	  grad_class  — name of the *Grad class (defined in Oa/Ml/Autograd.h)
	  inputs      — list of parameter names; populates SetGraphInputs in slot order
	  saved       — list of parameter names; populates Saved_ in vector order
	  ctor_args   — list of expressions passed to OaMakeSharedPtr<grad_class>(...)

	Returns the empty string when the op has no autograd block.
	"""
	ag = op.get("autograd")
	if not ag or not ag.get("grad_class"):
		return ""
	grad_class = ag["grad_class"]
	inputs     = ag.get("inputs", [])
	saved      = ag.get("saved", [])
	ctor_args  = ag.get("ctor_args", [])
	if not inputs:
		fail(f"{op['name']}: autograd.inputs must be non-empty when grad_class is set")
		return ""
	req_check = " or ".join(f"{i}.RequiresGrad()" for i in inputs)
	ctor_call = "(" + ", ".join(ctor_args) + ")" if ctor_args else "()"
	saved_init = "{" + ", ".join(saved) + "}"
	inputs_init = "{" + ", ".join(inputs) + "}"
	semantic_attach = ""
	if semantic_operation is not None:
		semantic_attach = (
			"\t\tconst auto semanticAttached = OaFnAutograd::AttachSemantic(\n"
			f"\t\t\t_gradFn, {semantic_operation});\n"
			"\t\tif (not semanticAttached.IsOk()) return {};\n"
		)
	broadcast_operation = ""
	if ag.get("attach") == "broadcast_binary":
		broadcast_operation = (
			f"\t\t_gradFn->Op_ = OaBcastBinOp::{ag['broadcast_op']};\n"
		)
	return (
		f"\tif (OaFnAutograd::IsEnabled() and ({req_check})) {{\n"
		f"\t\tauto _gradFn = OaMakeSharedPtr<{grad_class}>{ctor_call};\n"
		f"{broadcast_operation}"
		f"\t\t_gradFn->Saved_ = OaVec<OaMatrix>{saved_init};\n"
		f"\t\t_gradFn->SetGraphInputs(OaVec<OaMatrix>{inputs_init});\n"
		f"\t\t_gradFn->SequenceNr_ = OaFnAutograd::NextSeq();\n"
		f"\t\t_gradFn->OutputShape_ = out.GetShape();  // tape normalizes upstream d to this; protects elementwise bwd against viewed-shape fan-out grads\n"
		f"{semantic_attach}"
		f"\t\tout.MutAutograd().GradFn = _gradFn;\n"
		f"\t\tout.SetRequiresGrad(true);\n"
		f"\t}}\n"
	)


def _semantic_attribute_cpp_values(op: dict) -> list[str]:
	values: list[str] = []
	scalar = op.get("scalar_param")
	for index, attribute in enumerate(semantic_attribute_specs(op)):
		if scalar is not None and index == 0:
			source = f"In{scalar['name']}"
		else:
			source = attribute.get("source")
			if not source:
				fail(
					f"{op['name']}: generated semantic attribute "
					f"{attribute['name']!r} requires source"
				)
		factory = _ATTRIBUTE_FACTORY_CPP[attribute["kind"]]
		values.append(
			f'OaOperationAttribute::{factory}("{attribute["name"]}", {source})'
		)
	return values


def emit_context_body(op: dict, namespace: str = "OaFnMatrix") -> str:
	"""Body of the context-based function (records to context).

	Skipped when body = manual_context (session recorders) or cpu_util
	(host-only utilities) — those bodies are handwritten in a separate .cpp.
	"""
	if op.get("body", "auto") in ("manual_context", "cpu_util"):
		return ""
	name = op["name"]
	body = op.get("body", "auto")
	loss_name = op.get("loss_name")
	name_stmt = f'\tSetLastName("{loss_name}");\n' if namespace == "OaFnLoss" and loss_name else ""
	if body == "cpp_expr":
		if op.get("cpp_body"):
			body_src = "\n".join(f"\t{line}" if line else "" for line in op["cpp_body"].splitlines())
		else:
			body_src = f"\treturn {op['cpp_expr']};"
		return (
			f"OaMatrix {namespace}::{name}({cpp_param_list(op, with_defaults=False)}) {{\n"
			f"{name_stmt}"
			f"{body_src}\n"
			f"}}\n"
		)
	immediate = op.get("immediate", {})
	alias = immediate.get("alias_of")
	if alias:
		# Alias path (Neg → Scale(-1))
		return (
			f"OaMatrix {namespace}::{name}(const OaMatrix& InA) {{\n"
			f"\treturn Scale(InA, {alias['scalar']}f);\n"
			f"}}\n"
		)
	kind = op["kind"]
	push_struct = "struct { OaU32 Count; } push{n};"
	
	# Autograd removed - no longer tracking differentiability
	# Handle bias_add_broadcast special case
	if body == "bias_add_broadcast":
		kind = "binary"
		mats_init = "{&out, &InB}"
		access = "{OaBufferAccess::ReadWrite, OaBufferAccess::Read}"
		push_struct = (
			"OaU32 rows = static_cast<OaU32>(InA.NumElements() / InA.Size(-1));\n"
			"\tOaU32 cols = static_cast<OaU32>(InA.Size(-1));\n"
			"\tstruct { OaU32 Rows; OaU32 Cols; } push{rows, cols};"
		)
	elif kind == "binary":
		mats_init = "{&InA, &InB, &out}"
		access = "{OaBufferAccess::Read, OaBufferAccess::Read, OaBufferAccess::Write}"
	elif kind in ("unary", "reduce_full"):
		mats_init = "{&InA, &out}"
		access = "{OaBufferAccess::Read, OaBufferAccess::Write}"
	elif kind == "unary_scalar":
		sp = op["scalar_param"]
		push_struct = (
			"struct { OaU32 Count; " + sp["type"] + " " + sp["push_field"]
			+ "; } push{n, In" + sp["name"] + "};"
		)
		mats_init = "{&InA, &out}"
		access = "{OaBufferAccess::Read, OaBufferAccess::Write}"
	elif kind == "nullary_scalar":
		sp = op["scalar_param"]
		push_struct = (
			"struct { OaU32 Count; " + sp["type"] + " " + sp["push_field"]
			+ "; } push{n, In" + sp["name"] + "};"
		)
		mats_init = "{&out}"
		access = "{OaBufferAccess::Write}"
	else:
		# Unsupported kind in autogen body
		return ""
	
	# Opt-in: push extra_params after Count, matching the manual shader's
	# PushConstants field order (schema key: push_extra_params = true).
	# OaU8 params are widened to OaU32 — Slang push fields are 4-byte `uint`,
	# and a 1-byte C++ field would leave the shader reading stale padding.
	push_extras = (
		op.get("push_extra_params", False)
		and op.get("extra_params")
		and kind in ("unary", "binary")
	)
	if push_extras:
		fields = "".join(
			f" {'OaU32' if ep['type'] == 'OaU8' else ep['type']} {ep['name']};"
			for ep in op["extra_params"]
		)
		inits = "".join(
			f", static_cast<OaU32>(In{ep['name']})" if ep["type"] == "OaU8"
			else f", In{ep['name']}"
			for ep in op["extra_params"]
		)
		push_struct = "struct { OaU32 Count;" + fields + " } push{n" + inits + "};"

	semantic_record = ""
	semantic_operation = None
	if "contract" in op:
		if kind == "binary":
			semantic_inputs = "{&InA, &InB}"
		elif kind in ("unary", "unary_scalar", "reduce_full"):
			semantic_inputs = "{&InA}"
		elif kind == "nullary_scalar":
			semantic_inputs = "{}"
		else:
			fail(f"{name}: unsupported generated semantic operation kind {kind!r}")
		attribute_values = _semantic_attribute_cpp_values(op)
		semantic_attributes = "{" + ", ".join(attribute_values) + "}"
		semantic_record = (
			f"\tconst auto semantic = ctx.RecordOperation(\n"
			f"\t\tOaOperationRegistry::{name}, {semantic_inputs}, {{&out}},\n"
			f"\t\t{semantic_attributes});\n"
			"\tif (not semantic.IsOk()) return {};\n"
		)
		semantic_operation = "semantic.GetValue()"

	# Autograd attach: appended inside the function body, right before `return out;`
	autograd_block = emit_autograd_attach(op, semantic_operation)

	# Mark extra params as unused if we don't consume them
	ignore_extra = "" if push_extras else "".join(
		f"\t(void)In{ep['name']};\n" for ep in op.get("extra_params", [])
	)
	out_alloc = _output_alloc_stmt(op, namespace)
	copy_prefix = ""
	if body == "bias_add_broadcast":
		copy_dispatch = (
			",\n\t\t1, 1, "
			f"OaOperationRegistry::{name}.Name, 0,\n\t\t"
			f"OaOperationRegistry::{name}.Hash, 0, 0, semantic.GetValue()"
			if semantic_operation is not None else ""
		)
		copy_prefix = (
			"\tstruct { OaU32 Count; } copyPush{n};\n"
			"\tOaBufferAccess copyAccess[] = {OaBufferAccess::Read, OaBufferAccess::Write};\n"
			"\tctx.Add(\"Copy\", {&InA, &out}, copyAccess, &copyPush, "
			f"sizeof(copyPush), DivCeil(n, 256){copy_dispatch});\n\n"
		)
	workgroups_expr = _dispatch_workgroups_expr(op)
	if body == "bias_add_broadcast":
		workgroups_expr = "DivCeil(rows * cols, 256)"
	
	# Kernels dispatched over a shape-changing output must receive that same
	# output element count in their bounds push. Passing the input count here
	# made half-width gated activations (SiluMul/Geglu) write one complete output
	# tensor past their allocation even though their dispatch group count was
	# correctly derived from the output.
	n_expr = (
		"static_cast<OaU32>(out.NumElements())"
		if kind == "nullary_scalar" or op.get("dispatch_workgroups") == "output_elemwise"
		else "static_cast<OaU32>(InA.NumElements())"
	)
	dispatch_identity = (
		",\n\t\t1, 1, "
		f"OaOperationRegistry::{name}.Name, 0,\n\t\t"
		f"OaOperationRegistry::{name}.Hash, 0, 0, semantic.GetValue()"
		if semantic_operation is not None else ""
	)
	
	return (
		f"OaMatrix {namespace}::{name}({cpp_param_list(op, with_defaults=False)}) {{\n"
		f"\tauto& ctx = OaContext::GetDefault();\n"
		f"{ignore_extra}"
		f"\t{out_alloc}\n"
		f"{semantic_record}"
		f"\tOaU32 n = {n_expr};\n"
		f"\n"
		f"{copy_prefix}"
		f"\t{push_struct}\n"
		f"\tOaBufferAccess access[] = {access};\n"
		f'\tctx.Add("{op["kernel_forward"]}", {mats_init}, access, &push, sizeof(push), '
		f"{workgroups_expr}{dispatch_identity});\n"
		f"{autograd_block}"
		f"\treturn out;\n"
		f"}}\n"
	)


def emit_cpp_file(ops: list[dict], schema_name: str, category: str,
                  domain: str = "Core", namespace: str | None = None) -> str:
	if namespace is None:
		namespace = DOMAIN_NAMESPACE.get(domain, "OaFnMatrix")
	out: list[str] = []
	out.append(GEN_HEADER.format(schema_name=schema_name))
	out.append(f"// {namespace} — {category} GPU operations.\n")
	out.append("// Context-based unified API: records operations to OaContext.\n\n")
	any_autograd = any(
		op.get("autograd", {}).get("grad_class")
		and op.get("body", "auto") not in ("manual_context", "cpu_util")
		for op in ops
	)
	any_contract = any(
		"contract" in op
		and op.get("body", "auto") not in ("manual_context", "cpu_util")
		for op in ops
	)
	if any_autograd:
		out.append("#include <Oa/Ml/Autograd/Nodes.h>\n")
	out.append("#include <Oa/Core/Matrix.h>\n")
	out.append("#include <Oa/Core/FnMatrix.h>\n")
	if any_contract:
		out.append("#include <Oa/Core/Operation.h>\n")
	# Public header for the target namespace in this domain, when one exists.
	# Keyed by (domain, namespace) — Core/Ml/Ui all use OaFnMatrix but live in
	# different headers.
	ns_header = NAMESPACE_HEADER.get((domain, namespace))
	if ns_header:
		out.append(f"#include <{ns_header}>\n")
	out.append("#include <Oa/Core/Status.h>\n")
	out.append("#include <Oa/Core/Types.h>\n")
	out.append("#include <Oa/Core/BufferAccess.h>\n")
	out.append("#include <Oa/Runtime/Context.h>\n")
	out.append("#include <Oa/Runtime/Engine.h>\n")
	out.append("#include <Oa/Core/Validation.h>\n\n")
	out.append("#include <cassert>\n\n")
	needs_div_ceil = any(
		op.get("body", "auto") not in ("manual_context", "cpu_util", "cpp_expr")
		for op in ops
	)
	if needs_div_ceil:
		out.append("static OaU32 DivCeil(OaU32 InA, OaU32 InB) { return (InA + InB - 1) / InB; }\n\n")
	for op in ops:
		# Manual implementations can still generate their mechanical global-engine
		# forwarding overload. Validation/dispatch/composition remains hand-written.
		body = emit_manual_forwarder(op, namespace)
		if not body:
			body = emit_context_body(op, namespace)
		if body:
			hdr = f"// ─── {op['name']} ({op.get('notes', '').rstrip('.')}) " + "─" * 4
			out.append(hdr[:78].rstrip() + "\n\n")
			out.append(body + "\n")
	# Generated translation units have exactly one final newline. In particular,
	# all-manual schemas must not accumulate a second blank line at EOF.
	return "".join(out).rstrip() + "\n"


