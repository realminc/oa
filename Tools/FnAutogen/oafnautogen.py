# Operand symbols in forward_op → host array expressions for the CPU reference.
# Single-letter `a`/`b` are surrounded by `\b` (word boundary) so abs() etc.
# are not mangled. pc.<Field> → scalar (we pass via constant `kScalar`).
def _cpu_ref_expr(forward_op: str) -> str:
	expr = forward_op
	expr = re.sub(r"\bpc\.[A-Za-z_][A-Za-z0-9_]*\b", "kScalar", expr)
	expr = re.sub(r"\b(abs|log|sqrt|pow|max|min|exp|tanh)\b", r"std::\1", expr)
	# After std:: prefix, expand operand letters.
	expr = re.sub(r"(?<![A-Za-z_])a(?![A-Za-z_0-9])", "a_host[i]", expr)
	expr = re.sub(r"(?<![A-Za-z_])b(?![A-Za-z_0-9])", "b_host[i]", expr)
	return expr


def _emit_fn_test_call(op: dict) -> str:
	"""Emits the OaFnMatrix call for context-based API."""
	name = op["name"]
	kind = op["kind"]
	if kind == "binary":
		args = "a, b"
	elif kind in ("unary", "reduce_full"):
		args = "a"
	elif kind == "unary_scalar":
		args = "a, kScalar"
	elif kind == "nullary_scalar":
		# Factory convention: shape first, then value (Fill(shape, value)).
		test_config = op.get("test", {})
		scalar_value = test_config.get("scalar_value", "1.0f")
		args = f"OaMatrixShape{{N}}, {scalar_value}"
	else:
		return ""
	# Context is default parameter, so we don't need to pass it explicitly
	return f"OaFnMatrix::{name}({args})"


def _emit_fn_test(op: dict) -> str:
	"""High-level test: exercise OaFnMatrix::Op(...) with context API."""
	name = op["name"]
	kind = op["kind"]
	fwd = op.get("forward_op")
	# Check for test config - if present, use it; otherwise use forward_op
	test_config = op.get("test", {})
	if test_config.get("skip_test", False):
		return ""
	# If no test config and no forward_op, skip
	if not test_config and not fwd:
		return ""
	
	# Get CPU reference from test config or derive from forward_op
	cpu_ref = test_config.get("cpu_reference")
	if not cpu_ref:
		if not fwd:
			return ""
		cpu_ref = _cpu_ref_expr(fwd)
	
	# Get tolerance from test config or use defaults
	tolerance = test_config.get("tolerance", "1e-3f" if name in ("Log", "Sqrt", "Pow") else "1e-4f")
	test_size = test_config.get("test_sizes", [256])[0]
	
	positive_input = name in ("Log", "Sqrt", "Pow")
	rand_a = f"\tauto a = OaFnMatrix::Rand(OaMatrixShape{{N}});\n"
	if positive_input:
		rand_a = f"\tauto a = OaFnMatrix::Rand(OaMatrixShape{{N}});  // [0, 1)\n"
	
	# For nullary_scalar, we don't need input 'a'
	if kind == "nullary_scalar":
		rand_a = ""
	
	scalar_decl = ""
	if kind == "unary_scalar":
		scalar_value = test_config.get("scalar_value", "1.5f")
		scalar_decl = f"\tconstexpr OaF32 kScalar = {scalar_value};\n"
	
	binary_setup = ""
	if kind == "binary":
		binary_setup = (
			"\tauto b = OaFnMatrix::Rand(OaMatrixShape{N});\n"
			"\tstd::vector<float> b_host(N);\n"
			"\tASSERT_TRUE(OaFnMatrix::CopyToHost(b, b_host.data(), N * sizeof(float)).IsOk());\n"
		)
	
	# Select the recorder only; host reads in generated tests provide the explicit
	# observation boundary without destructor submission.
	context_setup = "\tOaContext::RecordingScope ctx_scope(OaContext::GetDefault());\n"
	
	# Handle reduce_full operations differently
	if kind == "reduce_full":
		return (
			f"TEST_VK(TestFnMatrix{op['category']}, {name}_Context) {{\n"
			f"\tconstexpr OaU32 N = {test_size};\n"
			f"{rand_a}"
			f"{context_setup}"
			f"\tauto out = {_emit_fn_test_call(op)};\n"
			f"\t// Context scope auto-executes and syncs at end of scope\n"
			f"\tstd::vector<float> a_host(N);\n"
			f"\tASSERT_TRUE(OaFnMatrix::CopyToHost(a, a_host.data(), N * sizeof(float)).IsOk());\n"
			f"\tfloat want = {cpu_ref};\n"
			f"\tfloat got = 0.0f;\n"
			f"\tASSERT_TRUE(OaFnMatrix::CopyToHost(out, &got, sizeof(float)).IsOk());\n"
			f"\tEXPECT_NEAR(got, want, {tolerance}) << \"Reduction result mismatch\";\n"
			f"}}\n\n"
		)
	
	# Element-wise operations
	# For nullary operations, we don't need to copy input 'a'
	copy_a_host = ""
	if kind != "nullary_scalar":
		copy_a_host = (
			"\tstd::vector<float> a_host(N);\n"
			"\tASSERT_TRUE(OaFnMatrix::CopyToHost(a, a_host.data(), N * sizeof(float)).IsOk());\n"
		)
	
	return (
		f"TEST_VK(TestFnMatrix{op['category']}, {name}_Context) {{\n"
		f"\tconstexpr OaU32 N = {test_size};\n"
		f"{scalar_decl}"
		f"{rand_a}"
		f"{binary_setup}"
		f"{context_setup}"
		f"\tauto out = {_emit_fn_test_call(op)};\n"
		f"\t// Context scope auto-executes and syncs at end of scope\n"
		f"{copy_a_host}"
		f"\tstd::vector<float> want(N);\n"
		f"\tfor (OaU32 i = 0; i < N; ++i) want[i] = {cpu_ref};\n"
		f"\tstd::vector<float> got(N);\n"
		f"\tASSERT_TRUE(OaFnMatrix::CopyToHost(out, got.data(), N * sizeof(float)).IsOk());\n"
		f"\tfor (OaU32 i = 0; i < N; ++i) EXPECT_NEAR(got[i], want[i], {tolerance}) << \"i=\" << i;\n"
		f"}}\n\n"
	)


def _emit_backward_test(op: dict) -> str:
	"""Per-op backward smoke test: sets requires_grad, runs forward + Sum + Backward."""
	ag = op.get("autograd", {})
	if not ag.get("differentiable"):
		return ""
	# Skip backward tests for manual ops with complex signatures (extra_params)
	if op.get("body") == "manual_context" and op.get("extra_params"):
		return ""
	name = op["name"]
	kind = op["kind"]
	if kind == "binary":
		setup = (
			"\tauto a = OaFnMatrix::Full(OaMatrixShape{8}, 2.0f);\n"
			"\tauto b = OaFnMatrix::Full(OaMatrixShape{8}, 3.0f);\n"
			"\ta.SetRequiresGrad(true);\n"
			"\tb.SetRequiresGrad(true);\n"
		)
		fwd = f"OaFnMatrix::{name}(a, b)"
		checks = (
			'\tEXPECT_TRUE(a.HasGrad()) << "A should have grad after backward";\n'
			'\tEXPECT_TRUE(b.HasGrad()) << "B should have grad after backward";\n'
		)
	elif kind == "unary":
		setup = (
			"\tauto a = OaFnMatrix::Full(OaMatrixShape{8}, 2.0f);\n"
			"\ta.SetRequiresGrad(true);\n"
		)
		fwd = f"OaFnMatrix::{name}(a)"
		checks = '\tEXPECT_TRUE(a.HasGrad()) << "A should have grad after backward";\n'
	elif kind == "unary_scalar":
		setup = (
			"\tauto a = OaFnMatrix::Full(OaMatrixShape{8}, 2.0f);\n"
			"\ta.SetRequiresGrad(true);\n"
			"\tconstexpr OaF32 kScalar = 1.5f;\n"
		)
		fwd = f"OaFnMatrix::{name}(a, kScalar)"
		checks = '\tEXPECT_TRUE(a.HasGrad()) << "A should have grad after backward";\n'
	else:
		return ""
	return (
		f"TEST_VK(TestFnMatrix{op['category']}, {name}_Backward) {{\n"
		f"{setup}"
		f"\tauto c = OaFnMatrix::Sum({fwd});\n"
		f"\tc.Backward();\n"
		f"{checks}"
		f"}}\n\n"
	)


def emit_fn_tests(ops: list[dict], schema_name: str, category: str, domain: str) -> str:
	# Include ML header for ML domain tests
	ml_include = "#include <Oa/Ml/FnMatrix.h>\n" if domain == "Ml" else ""
	body = TEST_FN_PREAMBLE.format(schema_name=schema_name, category=category, ml_include=ml_include)
	for op in ops:
		# Single context-based test per op (replaces 3 mode tests)
		body += _emit_fn_test(op)
		body += _emit_backward_test(op)
	return body


def op_slang_path(op: dict, out_root: Path, *, live: bool) -> Path:
	"""Per-op .slang output path (only for ops that generate Slang)."""
	rel = op.get("slang_path", "Core/Shader/Compute/Flat").strip("/")
	name = op["name"]
	# Always use .gen.slang suffix to avoid overwriting manual .slang files
	suffix = ".gen.slang"
	# If slang_path already includes "Shader/Compute", use it directly
	if "Shader/Compute" in rel:
		return out_root / "Private" / "Oa" / rel / f"{name}{suffix}"
	# Otherwise, add Shader/Compute for backward compatibility
	parts = rel.split("/", 1)
	domain = parts[0]
	subdir = parts[1] if len(parts) > 1 else ""
	if subdir:
		return out_root / "Private" / "Oa" / domain / "Shader" / "Compute" / subdir / f"{name}{suffix}"
	return out_root / "Private" / "Oa" / domain / "Shader" / "Compute" / f"{name}{suffix}"


