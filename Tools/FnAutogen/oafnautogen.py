GEN_HEADER = (
	"// AUTO-GENERATED by Tools/FnAutogen/oafnautogen.py — DO NOT EDIT.\n"
	"// Source of truth: Tools/FnAutogen/Schema/{schema_name}.\n"
	"// Regenerate via: python3 Tools/FnAutogen/oafnautogen.py --live\n"
)


def extra_param_decls(op: dict) -> list[str]:
	"""Returns C++ param decls for non-tensor scalar/dim args, e.g. `OaI32 InDim = -1`."""
	out = []
	for ep in op.get("extra_params", []):
		decl = f"{ep['type']} In{ep['name']}"
		if "default" in ep:
			decl = f"{decl} = {ep['default']}"
		out.append(decl)
	return out


def cpp_param_list(op: dict, *, with_defaults: bool = True) -> str:
	"""Build the parameter list for a context-based forward overload.

	with_defaults: emit `= -1` defaults in declarations, but not in definitions.
	Context is obtained automatically inside the function, not passed as parameter.
	"""
	parts: list[str] = []
	kind = op["kind"]
	if kind == "binary":
		parts += ["const OaMatrix& InA", "const OaMatrix& InB"]
	elif kind in ("unary", "reduce_full"):
		parts += ["const OaMatrix& InA"]
	elif kind == "unary_scalar":
		sp = op["scalar_param"]
		parts += ["const OaMatrix& InA", f"{sp['type']} In{sp['name']}"]
	elif kind == "nullary_scalar":
		# Factory convention: shape/extra params first, then the fill value —
		# matching Full(shape, value) and torch.full(size, fill_value). The
		# scalar is appended *after* the extra params below.
		sp = op["scalar_param"]
		for ep in extra_param_decls(op):
			if not with_defaults:
				ep = re.sub(r"\s*=\s*[^,]+$", "", ep)
			parts.append(ep)
		parts += [f"{sp['type']} In{sp['name']}"]
		return ", ".join(parts)
	# extra params (after main tensor args)
	for ep in extra_param_decls(op):
		if not with_defaults:
			ep = re.sub(r"\s*=\s*[^,]+$", "", ep)
		parts.append(ep)
	return ", ".join(parts)


def _api_param_parts(param: str | dict) -> tuple[str, str, str | None]:
	if isinstance(param, str):
		decl, separator, default = param.partition("=")
		decl = decl.strip()
		param_type, name = decl.rsplit(None, 1)
		return param_type, name, default.strip() if separator else None
	name = f"In{param['name']}"
	return param["type"], name, param.get("default")


def api_param_list(op: dict, *, with_defaults: bool = True) -> str:
	"""Exact schema-owned public parameter list for non-uniform manual ops."""
	parts = []
	for param in op.get("api_params", []):
		param_type, name, default = _api_param_parts(param)
		decl = f"{param_type} {name}"
		if with_defaults and default is not None:
			decl += f" = {default}"
		parts.append(decl)
	return ", ".join(parts)


def api_arg_list(op: dict) -> str:
	return ", ".join(_api_param_parts(param)[1] for param in op.get("api_params", []))


def header_fragment_for_op(op: dict, namespace: str = "OaFnMatrix") -> list[str]:
	name = op["name"]
	notes = op.get("notes", "")
	body = op.get("body", "auto")
	if not op.get("generate_declaration", True):
		return []
	if op.get("api_params"):
		lines = [f"\t/// {name}: {notes}"]
		return_type = op.get("api_return", "OaMatrix")
		params = api_param_list(op)
		if op.get("generate_engine_overload", False):
			engine_params = f"OaEngine& InRt, {params}" if params else "OaEngine& InRt"
			lines.append(f"\t[[nodiscard]] {return_type} {name}({engine_params});")
		lines.append(f"\t[[nodiscard]] {return_type} {name}({params});")
		lines.append("")
		return lines
	# Entirely handwritten operations own no generated declaration. Emitting a
	# comment-only placeholder makes the generated surface look non-empty and
	# leaves stale noise in otherwise useful mixed-category headers.
	if body in ("manual_context", "cpu_util"):
		return []
	lines = [f"\t/// {name}: {notes}"]
	lines.append(f"\t/// Context-based: auto y = {namespace}::{name}(...);")
	lines.append(f"\t/// Records operation to thread-local default context.")
	lines.append(f"\t[[nodiscard]] OaMatrix {name}({cpp_param_list(op)});")
	lines.append("")
	return lines


def emit_manual_forwarder(op: dict, namespace: str) -> str:
	"""Generate the global-engine convenience overload for a manual GPU body."""
	if not (op.get("api_params") and op.get("generate_forwarder", False)):
		return ""
	name = op["name"]
	return_type = op.get("api_return", "OaMatrix")
	params = api_param_list(op, with_defaults=False)
	args = api_arg_list(op)
	call_args = f"*OaEngine::GetGlobal(), {args}" if args else "*OaEngine::GetGlobal()"
	return (
		f"{return_type} {namespace}::{name}({params}) {{\n"
		f"\treturn {namespace}::{name}({call_args});\n"
		f"}}\n"
	)


def schema_label(schema_path: Path) -> str:
	try:
		return schema_path.resolve().relative_to(SCHEMA_DIR.resolve()).as_posix()
	except ValueError:
		return schema_path.name


def emit_header_fragment(ops: list[dict], schema_name: str, category: str, namespace: str = "OaFnMatrix") -> str:
	lines = [GEN_HEADER.format(schema_name=schema_name)]
	lines.append(f"// {namespace} — {category} overloads.\n")
	for op in ops:
		lines.extend(header_fragment_for_op(op, namespace))
	return "\n".join(lines)


